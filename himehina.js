'use strict';

////////////////////////////
//////////////////////////////
//////////////const////////////////////
//////////////////////////////
//////////////////////
const backgroundmusic = document.querySelector('.backgroundmusic');
const backgroundMusicIcon = document.querySelector('.backgroundmusic-icon');

///////////////////////////////////
/////////////////////////////
///////////ORDER//////////////////////////
///////////////////////////////////////
////////////////////////////////

///////////////////////////////////
/////////////////////////////
///////////BACKGROUNDMUSIC PUNCTION//////////////////////////
///////////////////////////////////////
////////////////////////////////
const playMusic = function () {
  backgroundmusic.classList.toggle('paused');
  backgroundmusic.play();
};

const pauseMusic = function () {
  backgroundmusic.classList.toggle('paused');
  backgroundmusic.pause();
};

backgroundMusicIcon.addEventListener('click', function () {
  const isMusicPlay = backgroundmusic.classList.contains('paused');
  isMusicPlay ? pauseMusic() : playMusic();
});
